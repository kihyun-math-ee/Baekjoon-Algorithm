import sys

N = int(sys.stdin.readline())
upper = (((2 * N) - 1) // 2) - 1

print((N * '*') + ((2 * N - 3 )* ' ') + (N * '*'))

for i in range(1, upper + 1):
    print((i * ' ') + '*' + ((N - 2) * ' ') + '*' + (((2 * N - 3) - (2 * i)) * ' ') + '*' + ((N - 2) * ' ') + '*')

print(((N - 1) * ' ') + '*' + ((N - 2) * ' ') + '*' + ((N - 2) * ' ') + '*')

for j in range(upper, 0, -1):
    print((j * ' ') + '*' + ((N - 2) * ' ') + '*' + (((2 * N - 3) - (2 * j)) * ' ') + '*' + ((N - 2) * ' ') + '*')

print((N * '*') + ((2 * N - 3 )* ' ') + (N * '*'))